String getGenerationName(int id) {
  switch (id) {
    case 1:
      return 'GENERATION I';
    case 2:
      return 'GENERATION II';
    case 3:
      return 'GENERATION III';
    case 4:
      return 'GENERATION IV';
    case 5:
      return 'GENERATION V';
    case 6:
      return 'GENERATION VI';
    case 7:
      return 'GENERATION VII';
    case 8:
      return 'GENERATION VIII';
    case 9:
      return 'GENERATION IX';
    default:
      return 'GENERATION I';
  }
}
