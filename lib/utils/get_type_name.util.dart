String getTypeName(int id) {
  switch (id) {
    case 1:
      return 'NORMAL';
    case 2:
      return 'FIGHTING';
    case 3:
      return 'FLYING';
    case 4:
      return 'POISON';
    case 5:
      return 'GROUND';
    case 6:
      return 'ROCK';
    case 7:
      return 'BUG';
    case 8:
      return 'GHOST';
    case 9:
      return 'STEEL';
    case 10:
      return 'FIRE';
    case 11:
      return 'WATER';
    case 12:
      return 'GRASS';
    case 13:
      return 'ELECTRIC';
    case 14:
      return 'PSYCHIC';
    case 15:
      return 'ICE';
    case 16:
      return 'DRAGON';
    case 17:
      return 'DARK';
    case 18:
      return 'FAIRY';
    case 19:
      return 'STELLAR';
    case 20:
      return 'UNKNOWN';
    default:
      return 'UNKNOWN';
  }
}
